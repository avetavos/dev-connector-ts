import { Schema, Document, model } from 'mongoose';
import User from '../interface/User';

const userSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});

const userModel = model<User & Document>('User', userSchema);

export default userModel;
